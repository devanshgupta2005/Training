import math

# 1. Square root of 625
sqrt_625 = math.sqrt(625)
print(f"Square root of 625: {sqrt_625}")

# 2. Factorial of 6
fact_6 = math.factorial(6)
print(f"Factorial of 6: {fact_6}")

# 3. Floor and ceiling values of 4.7
floor_4_7 = math.floor(4.7)
ceil_4_7 = math.ceil(4.7)
print(f"Floor of 4.7: {floor_4_7}")
print(f"Ceiling of 4.7: {ceil_4_7}")

# 4. 5 raised to the power 3
pow_5_3 = math.pow(5, 3)
print(f"5 raised to the power 3: {pow_5_3}")

# 5. Absolute value of -18 using math.fabs()
fabs_neg18 = math.fabs(-18)
print(f"Absolute value of -18: {fabs_neg18}")

# 6. Sine, cosine, and tangent of 90 degrees
deg_90_rad = math.radians(90)
sin_90 = math.sin(deg_90_rad)
cos_90 = math.cos(deg_90_rad)
tan_90 = math.tan(deg_90_rad)
print(f"Sine of 90 degrees: {sin_90}")
print(f"Cosine of 90 degrees: {cos_90}")
print(f"Tangent of 90 degrees: {tan_90}")

# 7. GCD of 36 and 60
gcd_36_60 = math.gcd(36, 60)
print(f"GCD of 36 and 60: {gcd_36_60}")

# 8. Value of e^3
e_pow_3 = math.exp(3)
print(f"e^3: {e_pow_3}")

# 9. Logarithm base 10 of 1000
log10_1000 = math.log10(1000)
print(f"Log base 10 of 1000: {log10_1000}")

# 10. Convert 180 degrees to radians
rad_180 = math.radians(180)
print(f"180 degrees in radians: {rad_180}")




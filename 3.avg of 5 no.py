# Program to calculate average of 5 persons

# Taking input from the user
p1 = float(input("Enter value of person 1: "))
p2 = float(input("Enter value of person 2: "))
p3 = float(input("Enter value of person 3: "))
p4 = float(input("Enter value of person 4: "))
p5 = float(input("Enter value of person 5: "))

# Calculating average
average = (p1 + p2 + p3 + p4 + p5) / 5

# Displaying result
print("Average =", average)
